  <h3>0. 문서의 목적</h3>

  이 문서는 CMSN 서비스(이하, "CMSN", "서비스", 또는 "세메센") 제공자와 서비스 이용자가 상호 동의하는 내용을 명시합니다. 귀하는 서비스를 이용함으로써 본 문서의 내용,약관,정책 및
  가이드라인(이하 총괄하여 “약관”)에 동의하는 것으로 간주됩니다. 서비스 이용 중 만일의 분쟁 발생 시 이 약관은 다른 어떠한 계약보다 우선하여 적용됩니다.
  <br><br>
  <h3>1. 서비스의 목적</h3>
  예술 작품 창작 의뢰를 주고받기 위해 필요한 홍보 공간과 기능을 제공하는 서비스입니다.<br><br>

  <h3>2. 용어의 정의</h3>
  커미션 : 예술 작품 창작을 의뢰하는 비용입니다. 또는 그 비용을 받고 하는 창작 행위입니다.<br>
  컨텐츠 : 텍스트, 이미지, 동영상, 기타 미디어를 포함하는 모든 내용입니다.<br>
  트윗 : 소셜 네트워크 서비스 'Twitter' 에서의 컨텐츠 게시 단위입니다.<br>
  답글 : 소셜 네트워크 서비스 'Twitter' 에서 어떤 트윗에 이어 다는 트윗입니다. 트윗에서 답글 아이콘을 클릭하거나 탭하면 답글을 달 수 있습니다. 답글을 클릭하거나 탭하면 답글이 달린 원래 트윗을 볼 수 있습니다.<br>
  멘션 : 소셜 네트워크 서비스 'Twitter' 에서 트윗 본문에 다른 사람의 사용자 아이디를 포함시키는 일, 또는 그러한 트윗을 지칭합니다. <br>
