  <h3>3. 이용자가 게시한 컨텐츠에 대한 권리</h3>
  3.1 귀하가 제공하는 컨텐츠<br>
  귀하의 모든 컨텐츠는 귀하의 소유입니다. 서비스에 컨텐츠를 제공할 때 귀하는 반드시 귀하의 소유인 컨텐츠임을 보증하고 제공합니다.
  세메센의 안내에 따라 귀하의 컨텐츠를 공공에 알리기 위한 서비스 이용 요청을 함으로써 귀하는 세메센이 귀하가 제공, 제출한 컨텐츠와 정보를 서비스의 목적 범위 내에서 분석하고 가공하여 세계의 이용자들이 사용하는
  웹 공간에 표출하도록 합니다. 표출할 때에는 축소 또는 축약하거나, 크기를 변경하거나, 열람하기 편리하도록 효과를 덧붙일 수도 있습니다.<br>
  3.2 귀하가 취득하는 컨텐츠<br>
  귀하는 본 서비스를 통해 게시되거나 귀하가 본 서비스를 통해 취득하는 컨텐츠의 이용과 신뢰에 대한 위험을 부담합니다. 컨텐츠에 대한 책임은 해당 컨텐츠의 게시를 서비스에 요청한 이용자가 전적으로 부담합니다.
  <br><br>
  <h3>4. 서비스의 이용과 한계</h3>
  서비스 이용 요청 중 일부는 트위터의 멘션을 통해 행해집니다. 이용자의 트윗이 트위터의 세메센 계정을 멘션할 때, 세메센 계정을 멘션하고 있는 트윗과 답글로 연결된 일련의 컨텐츠는 서비스에 제출된 것으로 여겨지며, 이 약관이 명시하는 범위 내에서 이용됩니다. 귀하의 서비스 이용은 귀하가 계정을 생성하고 컨텐츠를 제출하는 것에 대해 Twitter
  의 승인을 받고, 컨텐츠를 세메센에 제출하는 것에 대해 세메센에 의해 거부되지 않아야만 가능합니다. 세메센은 귀하의 서비스 접속을 거부하거나 제한할 수 있습니다. 귀하는 한 개의 트위터 계정에 한하여 서비스를 사용할 수 있습니다. 귀하가 이용하는 계정은 귀하만을 대표해야 합니다.<br>
  서비스 제공자는 본 서비스를 항상 제공하기 위하여 최선의 노력을 기울일 것입니다. 하지만 귀하는 어떠한 이유로든 본 사이트에 액세스하지 못하거나, 서비스에 제공한 정보가
  소멸되거나, 본 사이트가 작동하지 않을 수 있음을 인지하고 이에 동의합니다. 귀하는 제공자가 본 사이트 또는 서비스의 가용성을 통제할 수 없는 상황이 존재하며, 제공자가 그와 관련하여 귀하 또는 기타 제삼자에게
  책임을 지지 않음을 인정하고 이에 동의합니다.
  <br><br>
  <h3>5. 서비스의 유지</h3>
  세메센의 유지에는 비용이 필요하며, 이 비용의 일부는 광고 수익을 통해 얻어집니다. 귀하는 귀하가 세메센에 제출한 컨텐츠의 주변에, 세메센 서비스 제공자와 스폰서가 광고를 게시할 수 있음에 동의합니다. 광고가
  게시될 때에는 반드시, 이용자가 게시한 컨텐츠와 구별되도록 광고라는 것을 명시하고 서비스 관리자 또는 자동 광고 게시 프로그램이 게시합니다. 이용자는 본인의 커미션 컨텐츠가 아닌 것을 광고할 수 없습니다.
